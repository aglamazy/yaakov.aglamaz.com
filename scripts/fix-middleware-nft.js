/**
 * Next.js 16 with Turbopack bundles middleware into edge chunks and does not
 * emit a top-level middleware.js or middleware.js.nft.json.  Vercel's deploy
 * step still expects the nft.json file to exist.  This script reads the
 * middleware-manifest.json, creates a stub middleware.js (re-exporting the
 * edge bundle), and writes the nft.json that lists the bundled chunk files.
 */
const fs = require('fs');
const path = require('path');

const serverDir = path.join(__dirname, '..', '.next', 'server');
const nftPath = path.join(serverDir, 'middleware.js.nft.json');
const stubPath = path.join(serverDir, 'middleware.js');
const manifestPath = path.join(serverDir, 'middleware-manifest.json');

// Always write a proper middleware.js that exports a passthrough function.
// Vercel's ___next_launcher.cjs loads this and calls it as `middlewareHandler`.
// The real middleware logic runs as an edge function via the edge chunks
// listed in middleware-manifest.json — this stub just prevents the
// "middlewareHandler is not a function" error in the serverless context.
fs.writeFileSync(
  stubPath,
  `// Vercel Node.js middleware compatibility stub.
// The real middleware runs as an edge function via the Turbopack edge chunks
// in middleware-manifest.json. This stub satisfies Vercel's middleware-launcher.cjs
// which calls: middlewareHandler({ request }) and expects { response } back.
// Returning x-middleware-next:1 signals "pass through to the page handler".
module.exports = {
  default: async function middleware() {
    return {
      response: new Response(null, {
        status: 200,
        headers: { 'x-middleware-next': '1' }
      })
    };
  }
};
`
);
console.log('Wrote middleware.js passthrough stub.');

if (!fs.existsSync(manifestPath)) {
  console.log('No middleware-manifest.json found, skipping nft.json generation.');
  process.exit(0);
}

// If middleware.js.nft.json already exists (generated by Turbopack), nothing more to do.
if (fs.existsSync(nftPath)) {
  console.log('middleware.js.nft.json already exists, skipping nft.json generation.');
  process.exit(0);
}

const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
const entry = manifest.middleware && manifest.middleware['/'];

if (!entry) {
  console.log('No middleware entry in manifest, skipping nft.json generation.');
  process.exit(0);
}

// Collect all files referenced by the middleware.
// Manifest paths are relative to .next/ (e.g. "server/edge/chunks/foo.js").
// nft.json paths must be relative to the nft.json location (.next/server/).
const files = (entry.files || []).map(f => {
  const rel = path.relative('server', f);
  return rel;
});

// Write the nft.json
fs.writeFileSync(nftPath, JSON.stringify({ version: 1, files }, null, 2) + '\n');
console.log(`Created ${path.basename(nftPath)} with ${files.length} traced files.`);
